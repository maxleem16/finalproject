package com.example.domain;

import java.util.List;

public class DogVO{
	private String dogCode;
	private int age;
	private String gender;
	private String species;
	private String color;
	private String size;
	private String inoculationStatus;
	private String dogImage;
	private String feature;
	private String neutralizationStatus;
	private String information;
	private List<String> images;
	private String adoptStatus;
	public String getDogCode() {
		return dogCode;
	}
	public void setDogCode(String dogCode) {
		this.dogCode = dogCode;
	}
	public int getAge() {
		return age;
	}
	public void setAge(int age) {
		this.age = age;
	}
	public String getGender() {
		return gender;
	}
	public void setGender(String gender) {
		this.gender = gender;
	}
	public String getSpecies() {
		return species;
	}
	public void setSpecies(String species) {
		this.species = species;
	}
	public String getColor() {
		return color;
	}
	public void setColor(String color) {
		this.color = color;
	}
	public String getSize() {
		return size;
	}
	public void setSize(String size) {
		this.size = size;
	}
	public String getInoculationStatus() {
		return inoculationStatus;
	}
	public void setInoculationStatus(String inoculationStatus) {
		this.inoculationStatus = inoculationStatus;
	}
	public String getDogImage() {
		return dogImage;
	}
	public void setDogImage(String dogImage) {
		this.dogImage = dogImage;
	}
	public String getFeature() {
		return feature;
	}
	public void setFeature(String feature) {
		this.feature = feature;
	}
	public String getNeutralizationStatus() {
		return neutralizationStatus;
	}
	public void setNeutralizationStatus(String neutralizationStatus) {
		this.neutralizationStatus = neutralizationStatus;
	}
	public String getInformation() {
		return information;
	}
	public void setInformation(String information) {
		this.information = information;
	}
	public List<String> getImages() {
		return images;
	}
	public void setImages(List<String> images) {
		this.images = images;
	}
	public String getAdoptStatus() {
		return adoptStatus;
	}
	public void setAdoptStatus(String adoptStatus) {
		this.adoptStatus = adoptStatus;
	}
	
	
}
